# Benutzereingabe für Länge und Breite des Rechtecks
länge = float(input("Gib die Länge des Rechtecks in m ein: "))
breite = float(input("Gib die Breite des Rechtecks in m ein: "))

# Flächeninhalt berechnen
flächeninhalt = länge * breite

# Ergebnis ausgeben
print(f"Der Flächeninhalt des Rechtecks mit Länge {länge}m und Breite {breite}m beträgt {flächeninhalt} m/2.")
